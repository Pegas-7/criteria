package pojo;

public class Language {

    private Long id;

    private String nameLng;

    public Language() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNameLng() {
        return nameLng;
    }

    public void setNameLng(String nameLng) {
        this.nameLng = nameLng;
    }
}
